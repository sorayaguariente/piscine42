void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	a;
	int	swap;

	i = 0;
	a = size - 1;
	while (i < (size / 2))
	{
		swap = tab[i];
		tab[i] = tab [a];
		tab [a] = swap;
		i++;
		a--;
	}
}
